// Arithmetic operator
console.log(5 + 4);
console.log(9 - 5);
console.log(3 * 4);
console.log(7 / 2);

// Mod operator
console.log(7 % 2);
console.log(12.3 % 5);

// Unary operator
let value = 2;
value++;

console.log(value);

++value;
console.log(value);

--value;
console.log(value);

value--;
console.log(value);